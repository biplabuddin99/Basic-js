/* Array Method */

let countries=['Mexico','France','USA','Denmark','Germany','Italy','Spain','Poland'];
console.log(countries.length);

/* Entry/Insert
1.Push  --------last entry
2.Unshift  ------first entry
*/
countries.push("Brazil");
countries.push("Argentina");
console.log(countries);

countries.unshift('Tunisia');
countries.unshift('Saudi Arobia');
console.log(countries);

/*Ejech Entry 
1.pop  --------last left
2.shift -------first left
*/
countries.pop();
console.log(countries);
countries.shift();
 console.log(countries);

